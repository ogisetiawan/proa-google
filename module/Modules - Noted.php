//? Perubahan: Mudah dan murah untuk diterapkan.
//? Jadwal: Fleksibel dan dapat disesuaikan.
//? Biaya: Dapat berubah di setiap iterasinya.
//? Kualitas: Ditingkatkan secara berkelanjutan melalui umpan balik.
